package nestedArray;


public class timeseries {

    private String fld;
    private String dt;
    private float value;

    public String getfld() {
        return fld;
    }

    public String getdt() {
        return dt;
    }

    public float getvalue() {
        return value;
    }

  
    public void setfld(String fil) {
        //this.fld=fld;
        this.fld = fil;
    }

    public void setdt(String dt) {
        this.dt = dt;
    }

    public void setValue(float value) {
        this.value = value;
    }


}
